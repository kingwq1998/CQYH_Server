-- 安全注册 C# 类型: import_type 失败(类名拼写错误 / C# 端重命名或移动命名空间)时, 只打警告并返回 nil 跳过该类型,
-- 不让单个坏类名抛异常中断整个 init.lua、连累全部脚本系统. print 已在 C# 侧映射到 主程.添加系统日志.
local 类型注册失败数 = 0
local function 安全注册类型(类名)
	local ok, t = pcall(luanet.import_type, 类名)
	if ok and t ~= nil then
		return t
	end
	类型注册失败数 = 类型注册失败数 + 1
	print("[Lua类型注册] 警告: 无法注册 '" .. 类名 .. "' (类名拼写错误, 或 C# 端已重命名/移动命名空间), 已跳过; 引用该类型的脚本功能将失效, 请核对.")
	return nil
end

