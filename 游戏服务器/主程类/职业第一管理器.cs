using System;
using 游戏服务器.数据类;
using 游戏服务器.模板类;

namespace 游戏服务器
{
	// 职业第一(从零原创, 照配置截图规格复刻):
	// 各职业战力榜(系统数据.xx战力排名)的榜首玩家在线时, 获得 Settings.职业第一编号 配置的专属 BUFF;
	// 榜首易主时撤销前任的 BUFF. 由主循环每秒调用, 内部节流到每 30 秒刷新一次. 默认机制关闭.
	public static class 职业第一管理器
	{
		// 下标按 游戏对象职业 枚举: 0战士 1法师 2刺客 3弓手 4道士 5龙枪
		private static readonly 角色数据[] 当前持有 = new 角色数据[6];

		private static DateTime 下次刷新 = DateTime.MinValue;

		public static void 刷新()
		{
			if (!Settings.职业第一机制 || 主程.当前时间 < 职业第一管理器.下次刷新)
			{
				return;
			}
			职业第一管理器.下次刷新 = 主程.当前时间.AddSeconds(30.0);
			系统数据 数据 = 系统数据.数据;
			if (数据 == null)
			{
				return;
			}
			职业第一管理器.处理职业(数据.战士战力排名, 游戏对象职业.战士);
			职业第一管理器.处理职业(数据.法师战力排名, 游戏对象职业.法师);
			职业第一管理器.处理职业(数据.刺客战力排名, 游戏对象职业.刺客);
			职业第一管理器.处理职业(数据.弓手战力排名, 游戏对象职业.弓手);
			职业第一管理器.处理职业(数据.道士战力排名, 游戏对象职业.道士);
			职业第一管理器.处理职业(数据.龙枪战力排名, 游戏对象职业.龙枪);
		}

		private static void 处理职业(列表监视器<角色数据> 榜单, 游戏对象职业 职业)
		{
			int 索引 = (int)职业;
			if (索引 < 0 || 索引 >= 6)
			{
				return;
			}
			ushort buff编号 = Settings.职业第一编号[索引];
			if (buff编号 == 0)
			{
				return;
			}
			角色数据 新第一 = (榜单 != null && 榜单.Count > 0) ? 榜单[0] : null;
			角色数据 旧第一 = 职业第一管理器.当前持有[索引];
			if (旧第一 != 新第一)
			{
				// 榜首易主: 撤销前任 BUFF(若仍在线)
				if (旧第一 != null && 旧第一.角色在线(out var 旧网络) && 旧网络.绑定角色 != null)
				{
					旧网络.绑定角色.移除Buff时处理(buff编号);
				}
				职业第一管理器.当前持有[索引] = 新第一;
			}
			// 当前榜首在线且尚未持有该 BUFF 时补发(防叠层, 兼容掉线/过期后重新登录)
			if (新第一 != null && 新第一.角色在线(out var 新网络) && 新网络.绑定角色 != null && !新网络.绑定角色.Buff列表.ContainsKey(buff编号))
			{
				新网络.绑定角色.添加Buff时处理(buff编号, 新网络.绑定角色);
			}
		}
	}
}
