using 游戏服务器.地图类;
using 游戏服务器.数据类;
using 游戏服务器.模板类;

namespace 游戏服务器
{
	// 首杀/首爆(从零原创, 照配置截图规格复刻):
	// 全服首个击杀某 BOSS 的玩家 / 首个爆出某贵重物品的玩家获得配置奖励(货币+道具, 可选邮件发放), 并全服公告.
	// 达成记录持久化在 系统数据.怪物首杀记录 / 装备首爆记录, 全服永久去重. 默认全部关闭.
	public static class 首杀首爆管理器
	{
		public static void 尝试怪物首杀(int 怪物编号, string 怪物名, 玩家实例 玩家)
		{
			if (!Settings.开启怪物首杀功能 || 玩家 == null)
			{
				return;
			}
			系统数据 数据 = 系统数据.数据;
			if (数据 == null || 数据.怪物首杀记录.ContainsKey(怪物编号))
			{
				return;
			}
			// 先登记去重再发奖; 若发奖抛异常则回滚登记, 避免该 BOSS 被"假首杀"永久占位、之后再也不补发.
			数据.怪物首杀记录[怪物编号] = 玩家.地图编号;
			try
			{
				首杀首爆管理器.发放奖励(玩家, Settings.开启怪物首杀货币, Settings.首杀货币类型, Settings.首杀货币数量, Settings.开启怪物首杀道具, Settings.首杀道具编号, Settings.首杀道具数量, Settings.开启首杀奖励邮件, "全服首杀奖励", $"恭喜你成为全服首个击杀[{怪物名}]的勇士!");
			}
			catch
			{
				数据.怪物首杀记录.Remove(怪物编号);
				throw;
			}
			网络服务网关.发送公告($"恭喜[{玩家.对象名字}]达成全服首杀[{怪物名}]!", 滚动播报: true);
		}

		public static void 尝试装备首爆(int 物品编号, string 物品名, 玩家实例 玩家)
		{
			if (!Settings.开启装备首爆功能 || 玩家 == null)
			{
				return;
			}
			系统数据 数据 = 系统数据.数据;
			if (数据 == null || 数据.装备首爆记录.ContainsKey(物品编号))
			{
				return;
			}
			// 先登记去重再发奖; 若发奖抛异常则回滚登记, 避免该物品被"假首爆"永久占位、之后再也不补发.
			数据.装备首爆记录[物品编号] = 玩家.地图编号;
			try
			{
				首杀首爆管理器.发放奖励(玩家, Settings.开启装备首爆货币, Settings.首爆货币类型, Settings.首爆货币数量, Settings.开启装备首爆道具, Settings.首爆道具编号, Settings.首爆道具数量, Settings.开启首爆奖励邮件, "全服首爆奖励", $"恭喜你成为全服首个爆出[{物品名}]的勇士!");
			}
			catch
			{
				数据.装备首爆记录.Remove(物品编号);
				throw;
			}
			网络服务网关.发送公告($"恭喜[{玩家.对象名字}]达成全服首爆[{物品名}]!", 滚动播报: true);
		}

		private static void 发放奖励(玩家实例 玩家, bool 发货币, int 货币类型, int 货币数量, bool 发道具, int 道具编号, int 道具数量, bool 走邮件, string 邮件标题, string 邮件正文)
		{
			if (发货币 && 货币数量 > 0)
			{
				玩家.玩家获得货币((游戏货币)货币类型, 货币数量);
			}
			if (发道具 && 道具编号 > 0 && 道具数量 > 0)
			{
				if (走邮件)
				{
					玩家.角色数据.发送邮件(null, 邮件标题, 邮件正文, 道具编号, 道具数量);
				}
				else
				{
					玩家.玩家获得物品(道具编号, 道具数量, 邮件标题);
				}
			}
		}
	}
}
