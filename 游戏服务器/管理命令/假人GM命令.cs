using 游戏服务器.模板类;

namespace 游戏服务器.管理命令
{
    /// <summary>
    /// 假人系统管理命令。在服务端"GM 命令"输入框或游戏内管理员聊天里输入:
    ///   @假人 状态  —— 打印 池/在线 总数与各图配额达成情况
    ///   @假人 重载  —— 重新载入 假人配置.csv / 假人地图配额.csv / 假人喊话.txt(配额/开关/喊话即时生效; 未启用→启用会补建池)
    ///   @假人 清空  —— 让全部假人立即下线
    ///   @假人 填充  —— 跳过节流, 立即按配额补满
    /// 用 只能后台执行 确保命令被 主程.外部命令 队列在主循环线程 drain 执行, 操作 假人网关 不与服务循环竞态。
    /// </summary>
    public sealed class 假人 : GM命令
    {
        [字段描述(0, 排序 = 0)]
        public string 操作;

        public override 执行方式 执行方式 => 执行方式.只能后台执行;

        public override void 执行命令()
        {
            switch (this.操作)
            {
                case "状态":
                    假人网关.报告状态();
                    break;
                case "重载":
                    假人网关.重载配置();
                    break;
                case "清空":
                    假人网关.全部下线();
                    break;
                case "填充":
                    假人网关.立即填满();
                    break;
                default:
                    主程.添加系统日志("[假人] 用法: @假人 状态|重载|清空|填充");
                    break;
            }
        }
    }
}
