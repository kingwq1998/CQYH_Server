using 游戏服务器.地图类;
using 游戏服务器.数据类;

namespace 游戏服务器.管理命令
{
	// 清空全服首杀/首爆去重记录, 让所有 BOSS/贵重物品可重新触发首杀/首爆奖励.
	// 主要用途: 修复"元宝奖励被写进 角色货币 死槽"的旧 bug 后, 之前被假首杀/假首爆占位的记录需重置才能补发; 也可用于开新区/活动重置.
	public sealed class 重置首杀记录 : GM在线命令
	{
		public override 执行方式 执行方式 => 执行方式.优先后台执行;

		public override void 执行命令()
		{
		}

		public override void 执行命令(玩家实例 管理员)
		{
			系统数据 数据 = 系统数据.数据;
			if (数据 == null)
			{
				管理员.发送系统消息("系统数据未就绪, 无法重置首杀记录.");
				return;
			}
			int 首杀数 = 数据.怪物首杀记录.Count;
			int 首爆数 = 数据.装备首爆记录.Count;
			数据.怪物首杀记录.Clear();
			数据.装备首爆记录.Clear();
			管理员.发送系统消息($"已重置全服首杀记录 {首杀数} 条、首爆记录 {首爆数} 条, 所有 BOSS/贵重物品可重新触发奖励.");
			主程.添加命令日志($"<= @重置首杀记录: 清空首杀 {首杀数} 条 / 首爆 {首爆数} 条 (操作者: {管理员.对象名字})");
		}
	}
}
